namespace SimpleFactory
{
    /// <summary>
    /// 定义一个接口（接口是通用的、抽象的、非具体的功能，定义行为
    /// 【简单的说：就是可以做什么】）
    /// </summary>
    public interface ITestApi
    {
        /// <summary>
        /// 定义了一个传入字符串的测试打印方法
        /// </summary>
        /// <param name="info">需传入的参数</param>
        void TestPrint(string info);
    }
}
