*	            3、何时选用接口？【通常情况下优先使用接口】
*	            4、使用接口的好处【可以将外部调用和内部实现隔离开；只要接口不变，
*	                内部实现的变化就不会影响到外部应用，从而使系统更加灵活，具有更好的拓展性和可维护性】
